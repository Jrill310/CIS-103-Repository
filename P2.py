import math
RecW = float ( input ('Width Of Rectangle: '))
RecH = float ( input ('Height of Rectangle: '))
RecArea = RecW * RecH
print (RecArea)
TriB = float ( input ('Base Of Triangle: '))
TriH = float ( input ('Height Of Trangle: '))
TriArea = 0.5 * TriB * TriH
print (TriArea)
CirR = float ( input ('Radius Of Circle: '))
CirArea = math.pi * (CirR ** 2)
print (CirArea)

